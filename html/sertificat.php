		<p><b>Мы предлагаем несколько видов туристических подарочных сертификатов:</b></p>
		<p>Сертификаты могут быть посвящены различным событиям, иметь различный вид и текст. Текст каждого сертификата оговаривается отдельно с заказчиком (дарителем).</p>
		<p>Виды сертификата:</p>
		<ol>
			<li>Именной сертификат</li>
			<li>В сертификате указано имя/имена лиц, имеющих право воспользоваться правами, гарантированными сертификатом</li>
			<li>Сертификат на предъявителя</li>
			<li>Сертификатом может воспользоваться любое предъявившее его лицо</li>
			<li>Корпоративный сертификат</li>
			<li>Сертификатом имеет право воспользоваться человек (люди), которых определяет корпоративный заказчик. Сертификат, используемый
				в качестве подарка во время проведения рекламных кампаний, акций по стимулированию сбыта (лотереи, розыгрыши, конкурсы и т. д.)
				поощрения сотрудников организации. Обычно такие сертификаты выполняются в безымянном виде. В тексте сертификата особо отмечается
				организация-даритель</li>
			<li>Сертификат на конкретный тур. Им может воспользоваться любой человек, предъявивший данный сертификат.</li>
		</ol>
