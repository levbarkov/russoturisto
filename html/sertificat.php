			<li>это широкий спектр возможностей и вариантов;</li>
			<li>это любая сумма без ограничения;</li>
			<li>это сюрприз;</li>
			<li>это идея подарка для коллективного мероприятия, например, свадьбы, юбилея или корпоративного подарка.</li>
